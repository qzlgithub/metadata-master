package com.mingdong.core.exception;

public class MetadataIOException extends MetadataException
{
    public MetadataIOException()
    {
        super();
    }

    public MetadataIOException(String message)
    {
        super(message);
    }

    public MetadataIOException(String message, Throwable cause)
    {
        super(message, cause);
    }
}
